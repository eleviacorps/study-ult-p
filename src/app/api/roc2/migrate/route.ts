interface MigrationResult {
  name: string;
  success: boolean;
  error?: string;
}

export async function POST(request: Request) {
  const log = logRequest("POST /api/roc2/migrate", null);

  try {
    if (!process.env.NEXT_PUBLIC_SUPABASE_URL) {
      return NextResponse.json({ error: "supabase_not_configured" }, { status: 501 });
    }

    const supabase = await createClient();
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) return NextResponse.json({ error: "unauthorized" }, { status: 401 });
    log.setMeta("userId", user.id);

    const { data: profile } = await supabase
      .from("profiles")
      .select("role")
      .eq("id", user.id)
      .maybeSingle();
    if (profile?.role !== "admin") {
      return NextResponse.json({ error: "forbidden", message: "Admin access required" }, { status: 403 });
    }

    const projectRef = process.env.SUPABASE_PROJECT_REF;
    const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

    if (!projectRef || !serviceKey) {
      // No Management API — return SQL for manual execution
      return NextResponse.json({
        migrated: false,
        requiresManual: true,
        message: "Set SUPABASE_PROJECT_REF and SUPABASE_SERVICE_ROLE_KEY for auto-execution, or copy the SQL below into Supabase SQL Editor.",
        migrations: MIGRATIONS.map((m) => ({ name: m.name, sql: m.sql })),
      });
    }

    const results: MigrationResult[] = [];

    for (const migration of MIGRATIONS) {
      try {
        const res = await fetch(
          `https://api.supabase.com/v1/projects/${projectRef}/sql`,
          {
            method: "POST",
            headers: {
              "Authorization": `Bearer ${serviceKey}`,
              "Content-Type": "application/json",
            },
            body: JSON.stringify({ query: migration.sql }),
          }
        );

        if (!res.ok) {
          const errBody = await res.text().catch(() => "unknown");
          results.push({
            name: migration.name,
            success: false,
            error: `API ${res.status}: ${errBody.slice(0, 300)}`,
          });
        } else {
          results.push({ name: migration.name, success: true });
        }
      } catch (err) {
        results.push({
          name: migration.name,
          success: false,
          error: err instanceof Error ? err.message : "unknown_error",
        });
      }
    }

    const allSuccess = results.every((r) => r.success);
    const failedCount = results.filter((r) => !r.success).length;

    log.setMeta("migrations", results.map((r) => `${r.name}=${r.success ? "ok" : "fail"}`).join(","));

    if (allSuccess) {
      await log.success(200, `All ${results.length} migrations completed`);
      return NextResponse.json({ migrated: true, results });
    }

    await log.warn(500, `${failedCount}/${results.length} migrations failed`);
    return NextResponse.json({ migrated: false, results }, { status: 500 });
  } catch (err) {
    await log.error("migration_failed", err);
    return NextResponse.json(
      { error: err instanceof Error ? err.message : "unknown_error" },
      { status: 500 }
    );
  }
}

export async function GET() {
  return NextResponse.json({
    migrations: MIGRATIONS.map((m) => ({
      name: m.name,
      file: m.file,
      sql: m.sql,
    })),
    autoExecAvailable: !!(process.env.SUPABASE_PROJECT_REF && process.env.SUPABASE_SERVICE_ROLE_KEY),
    instructions: "POST to run via Management API, or copy SQL from any response and paste into Supabase SQL Editor.",
  });
}
