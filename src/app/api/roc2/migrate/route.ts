/**
 * ROC2 Migration Runner
 *
 * POST /api/roc2/migrate — runs pending ROC2 migrations
 * GET  /api/roc2/migrate — lists available migrations
 *
 * Requires SUPABASE_PROJECT_REF + SUPABASE_SERVICE_ROLE_KEY env vars
 * for automatic SQL execution via the Supabase Management API.
 *
 * Without those vars, provides the SQL text for manual copy-paste
 * into the Supabase SQL Editor.
 *
 * Security: restricted to admin users.
 *
 * ── Setup ──────────────────────────────────────────────────────
 * 1. Go to supabase.com/dashboard → Project Settings → API
 * 2. Copy your Project Reference → set SUPABASE_PROJECT_REF
 * 3. Copy your service_role key → set SUPABASE_SERVICE_ROLE_KEY
 * 4. POST to this endpoint to run migrations automatically
 *
 * ── Manual alternative ─────────────────────────────────────────
 * Open Supabase SQL Editor and paste the SQL from the GET response.
 */

