-- 9. USER POINTS (for leaderboard)
create table if not exists user_points (
  user_id uuid references auth.users on delete cascade primary key,
  points integer not null default 0,
  updated_at timestamptz default now()
);

-- 10. USER NOTES (private per-user vault storage)
-- Each note preserves the file path + content so the parser/layout works unchanged.
create table if not exists user_notes (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  chapter text not null,
  subject text not null default '',
  path text not null,
  title text not null,
  content text not null,
  content_hash text,
  canonical_slug text,
  tags text[] default '{}',
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  unique (user_id, path)
);
alter table user_notes add column if not exists content_hash text;
alter table user_notes add column if not exists canonical_slug text;
create index if not exists idx_user_notes_user on user_notes(user_id);
create unique index if not exists idx_user_notes_user_content_hash on user_notes(user_id, content_hash) where content_hash is not null;
create index if not exists idx_user_notes_canonical_slug on user_notes(user_id, canonical_slug);

-- 11. CHAT MESSAGES (AI Tutor conversations)
create table if not exists chat_messages (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  session_id uuid not null,
  client_id text,
  role text not null,
  content text not null,
  created_at timestamptz default now()
);
alter table chat_messages add column if not exists client_id text;
create index if not exists idx_chat_messages_session on chat_messages(user_id, session_id);
create unique index if not exists idx_chat_messages_client_id on chat_messages(user_id, session_id, client_id) where client_id is not null;

-- 11A. CHAT SESSIONS (scoped AI memory roots)
create table if not exists chat_sessions (
  id uuid primary key,
  user_id uuid references auth.users on delete cascade not null,
  type text not null default 'concept_discussion',
  title text not null default 'New Chat',
  subject text,
  chapter text,
  scope jsonb not null default '{}',
  summary text default '',
  last_message_at timestamptz default now(),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);
create index if not exists idx_chat_sessions_user on chat_sessions(user_id, updated_at desc);
create index if not exists idx_chat_sessions_scope on chat_sessions using gin(scope);

-- 11B. CHAT CONTEXT SUMMARIES (compressed conversation memory)
create table if not exists chat_context_summaries (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  session_id uuid references chat_sessions(id) on delete cascade not null,
  summary text not null,
  covered_message_ids bigint[] default '{}',
  token_estimate integer default 0,
  created_at timestamptz default now()
);
create index if not exists idx_chat_context_summaries_session on chat_context_summaries(user_id, session_id, created_at desc);

-- 11C. INTERACTION MEMORY (retrievable scoped memories)
create table if not exists interaction_memory (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  session_id uuid references chat_sessions(id) on delete cascade,
  memory_type text not null,
  subject text,
  chapter text,
  content text not null,
  metadata jsonb not null default '{}',
  content_hash text not null,
  created_at timestamptz default now(),
  unique (user_id, content_hash)
);
create index if not exists idx_interaction_memory_scope on interaction_memory(user_id, memory_type, subject, chapter);

