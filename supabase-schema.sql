drop policy if exists "Own data only" on study_sessions;
create policy "Own data only" on study_sessions for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on quiz_scores;
create policy "Own data only" on quiz_scores for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on test_scores;
create policy "Own data only" on test_scores for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on activity_snapshots;
create policy "Own data only" on activity_snapshots for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on topic_accuracy;
create policy "Own data only" on topic_accuracy for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on weak_areas;
create policy "Own data only" on weak_areas for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on chapter_progress;
create policy "Own data only" on chapter_progress for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on user_points;
create policy "Own data only" on user_points for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on user_notes;
create policy "Own data only" on user_notes for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on chat_messages;
create policy "Own data only" on chat_messages for all using (auth.uid() = user_id);
drop policy if exists "Own data only" on study_streaks;
create policy "Own data only" on study_streaks for all using (auth.uid() = user_id);

