-- 1. PROFILES
create table if not exists profiles (
  id uuid references auth.users on delete cascade primary key,
  name text default '',
  username text,
  bio text default '',
  avatar_url text default '',
  instagram text default '',
  twitter text default '',
  github text default '',
  website text default '',
  onboarding_completed boolean not null default false,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);
alter table profiles add column if not exists onboarding_completed boolean not null default false;
alter table profiles add column if not exists role text not null default 'user';
create unique index if not exists idx_profiles_username on profiles(username) where username is not null;

-- 2. STUDY SESSIONS (daily minutes)
create table if not exists study_sessions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  date date not null,
  minutes integer not null default 0,
  created_at timestamptz default now(),
  unique (user_id, date)
);
create index if not exists idx_study_sessions_user on study_sessions(user_id);

-- 3. QUIZ SCORES
create table if not exists quiz_scores (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  date timestamptz not null,
  score integer not null,
  total integer not null,
  net_score integer not null,
  created_at timestamptz default now(),
  unique (user_id, date)
);
create index if not exists idx_quiz_scores_user on quiz_scores(user_id);

-- 4. TEST SCORES
create table if not exists test_scores (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  date timestamptz not null,
  score integer not null,
  total integer not null,
  chapter text not null,
  created_at timestamptz default now(),
  unique (user_id, date, chapter)
);
create index if not exists idx_test_scores_user on test_scores(user_id);

-- 5. ACTIVITY SNAPSHOTS
create table if not exists activity_snapshots (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  timestamp text not null,
  type text not null,
  score integer,
  total integer,
  label text,
  items jsonb default '[]',
  created_at timestamptz default now(),
  unique (user_id, timestamp, type)
);
create index if not exists idx_activity_user on activity_snapshots(user_id);

-- 6. TOPIC ACCURACY
create table if not exists topic_accuracy (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  topic text not null,
  correct integer not null default 0,
  total integer not null default 0,
  updated_at timestamptz default now(),
  unique (user_id, topic)
