-- md_bank: all authenticated users can read, only admins can write
drop policy if exists "All authenticated can read md_bank" on md_bank;
create policy "All authenticated can read md_bank" on md_bank for select using (auth.role() = 'authenticated');
drop policy if exists "Admins can insert md_bank" on md_bank;
create policy "Admins can insert md_bank" on md_bank for insert with check (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);
drop policy if exists "Admins can update md_bank" on md_bank;
create policy "Admins can update md_bank" on md_bank for update using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);
drop policy if exists "Admins can delete md_bank" on md_bank;
create policy "Admins can delete md_bank" on md_bank for delete using (
  exists (select 1 from profiles where id = auth.uid() and role = 'admin')
);

